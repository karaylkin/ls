// Нормализация подсказки: null / "-" / пустое -> "" (слот сохраняется, ровно 2 подсказки)
const norm = (h) => {
  if (h === null || h === undefined) return '';
  const s = String(h).trim();
  return s === '-' ? '' : s;
};

const questions = [];
const issues = [];

// Группы идут как ключи объекта ("1","2","20"...). Сплющиваем по возрастанию question-id.
for (const groupKey of Object.keys(raw)) {
  for (const q of raw[groupKey]) {
    const hints = [norm(q.hint1), norm(q.hint2)];

    // Дозаполняем пустые слоты подсказок из FILL
    const fill = FILL[q['question-id']];
    if (fill) {
      if (!hints[0] && fill.h1) hints[0] = fill.h1;
      if (!hints[1] && fill.h2) hints[1] = fill.h2;
    }
    const item = {
      id: q['question-id'],
      text: String(q.question).trim(),
      hints,                       // ровно 2 слота (раздел 3.5)
      answer: q.answer,
      comment: q['answer-comm'] ?? null,
    };
    questions.push(item);

    // Диагностика: чего может не хватать для движка
    if (typeof item.answer !== 'number') issues.push(`#${item.id}: ответ не число (${item.answer})`);
    if (!hints[0]) issues.push(`#${item.id}: пустая подсказка 1`);
    if (!hints[1]) issues.push(`#${item.id}: пустая подсказка 2`);
  }
}

questions.sort((a, b) => a.id - b.id);

const out = {
  version: 1,
  count: questions.length,
  questions,
};

writeFileSync(OUT, JSON.stringify(out, null, 2), 'utf8');

console.log(`Сконвертировано вопросов: ${questions.length}`);
console.log(`Записано в: ${OUT}`);
console.log(`\nДиагностика (${issues.length} замечаний):`);
for (const i of issues) console.log('  - ' + i);
